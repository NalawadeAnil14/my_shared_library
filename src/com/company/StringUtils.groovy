package com.company

class StringUtils {
    String reverse(String str) {
        return str.reverse()
    }

    String toUppe(String str) {
        return str.toUpperCase()
    }

    String toLower(String str) {
        return str.toLowerCase()
    }

    String capitalize(String str) {
        return str.capitalize()
    }

    String count(String str){
        return str.length()
    } 
}